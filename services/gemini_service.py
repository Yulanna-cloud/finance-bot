import os
import json
import logging
import google.generativeai as genai

logger = logging.getLogger(__name__)

# Инициализация Gemini
api_key = os.getenv("GEMINI_API_KEY")
if api_key:
    genai.configure(api_key=api_key)
else:
    logger.error("GEMINI_API_KEY не установлен в переменных окружения!")

def classify_text(text: str) -> dict:
    """Разбивает текст операции на составляющие с помощью Google Gemini."""
    default_response = {
        "тип": "расход",
        "сумма": None,
        "валюта": "RUB",
        "категория": "Прочее",
        "подкатегория": "",
        "магазин": "",
        "описание": text,
        "уверенность": 0.0
    }
    
    if not api_key:
        return default_response

    # Жесткий список категорий на основе вашей таблицы
    prompt = f"""Ты — эксперт по учету личных финансов. Твоя задача — разобрать текст сообщения и вернуть СТРОГО JSON-объект.

Выбирай категорию ТОЛЬКО из этого списка (основан на бюджете пользователя):
- Продукты (если супермаркеты, еда, Пятерочка, Вкусвилл)
- Кафе (рестораны, кофе, фастфуд)
- Авто (бензин, заправка, запчасти, мойка)
- Транспорт (такси, метро, автобус)
- Жилье (коммуналка, аренда, ремонт)
- Развлечения (кино, хобби, книги)
- Здоровье (аптека, врачи)
- Одежда (обувь, вещи)
- Прочее (если совсем не подходит ни один пункт выше)

Правила:
1. ТИП: "расход" или "доход".
2. СУММА: Обязательно найди число в тексте и запиши как число (не строку). Если в тексте есть цифры, сумма НЕ должна быть null.
3. ВАЛЮТА: "RUB".
4. МАГАЗИН: Название бренда с большой буквы (Пятерочка, Лукойл и т.д.), если есть в тексте.
5. ОПИСАНИЕ: Краткая суть траты на русском.

Формат ответа — ТОЛЬКО чистый JSON, без разметки markdown (без знаков ```), без лишнего текста.

Пример ответа:
{{"тип": "расход", "сумма": 1400, "валюта": "RUB", "категория": "Продукты", "подкатегория": "", "магазин": "Пятерочка", "описание": "Покупка продуктов", "уверенность": 0.95}}

Разбери следующее сообщение пользователя: "{text}"
"""

    try:
        model = genai.GenerativeModel('gemini-1.5-flash')
        response = model.generate_content(prompt)
        
        # Очистка ответа от возможных строк разметки
        raw = response.text.strip()
        raw = raw.replace("```json", "").replace("```", "").strip()

        result = json.loads(raw)
        
        # Подстраховка: если ИИ выдал категорию не из списка, откатываем на Прочее
        allowed_categories = ["Продукты", "Кафе", "Авто", "Транспорт", "Жилье", "Развлечения", "Здоровье", "Одежда", "Прочее"]
        if result.get("категория") not in allowed_categories:
            result["категория"] = "Прочее"
            
        return result
        
    except Exception as e:
        logger.error(f"Ошибка в gemini_service: {e}")
        return default_response
