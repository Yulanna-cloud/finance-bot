def classify_text(text: str) -> dict:

    default_response = {
        "тип": "расход",
        "сумма": 0,
        "валюта": "RUB",
        "категория": "Прочее",
        "подкатегория": "",
        "магазин": "",
        "описание": text,
        "уверенность": 0.0
    }

    if not api_key:
        return default_response

    prompt = f"""
Ты — AI для учета личных финансов.

ТВОЯ ЗАДАЧА:
Разобрать сообщение пользователя и вернуть ТОЛЬКО JSON.

ВАЖНО:
Категорию выбирай СТРОГО из списка ниже.

КАТЕГОРИИ:

- Продукты
Примеры:
Пятерочка
Магнит
Вкусвилл
Перекресток
Лента
Ашан
Самокат
Яндекс Лавка
супермаркет
еда
продукты

- Кафе
Примеры:
кофе
ресторан
бургер
шаверма
суши
пицца
кафе

- Авто
Примеры:
бензин
заправка
мойка
автосервис

- Транспорт
Примеры:
такси
метро
автобус

- Жилье
Примеры:
аренда
коммуналка
ЖКХ

- Развлечения
Примеры:
кино
игры
книги

- Здоровье
Примеры:
аптека
врач

- Одежда
Примеры:
одежда
обувь

- Прочее
Только если ничего не подошло.

ОСОБО ВАЖНО:
Если видишь:
Пятерочка
Магнит
Вкусвилл
Перекресток
Лента
Ашан

ТО категория ОБЯЗАТЕЛЬНО:
"Продукты"

ФОРМАТ ОТВЕТА:

{{
  "тип": "расход",
  "сумма": 1400,
  "валюта": "RUB",
  "категория": "Продукты",
  "подкатегория": "",
  "магазин": "Пятерочка",
  "описание": "Продукты",
  "уверенность": 0.95
}}

Никакого markdown.
Никакого текста.
Только JSON.

Сообщение пользователя:
{text}
"""

    try:

        model = genai.GenerativeModel("gemini-1.5-flash")

        response = model.generate_content(prompt)

        if not response.text:
            raise Exception("Gemini вернул пустой ответ")

        raw = response.text.strip()

        raw = raw.replace("```json", "")
        raw = raw.replace("```", "")
        raw = raw.strip()

        logger.info(f"GEMINI RAW: {raw}")

        result = json.loads(raw)

        allowed_categories = [
            "Продукты",
            "Кафе",
            "Авто",
            "Транспорт",
            "Жилье",
            "Развлечения",
            "Здоровье",
            "Одежда",
            "Прочее"
        ]

        if result.get("категория") not in allowed_categories:
            result["категория"] = "Прочее"

        return result

    except Exception as e:

        logger.error(f"Ошибка Gemini: {e}")

        return default_response

