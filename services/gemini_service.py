import os
import json
import logging
import google.generativeai as genai

logger = logging.getLogger(__name__)

# Инициализация Gemini
api_key = os.getenv("GEMINI_API_KEY")
if api_key:
    genai.configure(api_key=api_key)
else:
    logger.error("GEMINI_API_KEY не установлен в переменных окружения!")

def classify_text(text: str) -> dict:
    """Разбивает текст операции на составляющие с помощью Google Gemini."""
    default_response = {
        "тип": "расход",
        "сумма": None,
        "валюта": "RUB",
        "категория": "Прочее",
        "подкатегория": "",
        "магазин": "",
        "описание": text,
        "уверенность": 0.0
    }
    
    if not api_key:
        return default_response

    # Используем обычную строку без f"", чтобы фигурные скобки примера не ломали Python
    prompt = """Ты — строгий и умный ассистент по учету личных финансов. 
Твоя задача — разобрать текст сообщения о трате или доходе и вернуть строго JSON-объект.

Правила классификации:
1. ТИП: "расход" или "доход".
2. СУММА: Найди число в тексте. Если число есть (например, "Бензин 1000" или "продукты Пятерочка 1400"), то сумма ОБЯЗАТЕЛЬНО должна быть этим числом (1000 или 1400). Никогда не возвращай null для суммы, если в тексте есть цифры!
3. ВАЛЮТА: Обычно "RUB".
4. КАТЕГОРИЯ: Подбери логичную категорию на русском языке (например: Продукты, Авто, Кафе, Зарплата, Развлечения, Здоровье, Одежда, Прочее). Если в тексте упоминается бензин или заправка — это категория "Авто". Если Пятерочка, Вкусвилл, супермаркет — это "Продукты".
5. МАГАЗИН: Если из текста понятен конкретный бренд или место (Пятерочка, Вкусвилл, Лукойл, Яндекс.Такси), запиши его название с большой буквы.
6. ОПИСАНИЕ: Краткое пояснение сути (например, "Бензин", "Продукты").

Формат ответа — ТОЛЬКО чистый JSON, без разметки markdown (без ```json ... ```), без лишнего текста.

Пример текста: "продукты Пятерочка 1400"
Пример ответа:
{"тип": "расход", "сумма": 1400, "валюта": "RUB", "категория": "Продукты", "подкатегория": "", "магазин": "Пятерочка", "описание": "Продукты", "уверенность": 0.95}

Разбери следующее сообщение: """ + f'"{text}"'

    try:
        model = genai.GenerativeModel('gemini-1.5-flash')
        response = model.generate_content(prompt)
        
        raw = response.text.strip()
        if raw.startswith("```"):
            raw = raw.split("\n", 1)[1]
        if raw.endswith("```"):
            raw = raw.rsplit("\n", 1)[0]
        raw = raw.replace("```json", "").strip()

        result = json.loads(raw)
        return result
        
    except Exception as e:
        logger.error(f"Ошибка в gemini_service: {e}")
        return default_response
